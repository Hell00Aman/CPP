#include<iostream>
using namespace std;

int main() {
    // Declare and initialize an array with 5 elements
    int arr[5] = {1, 23, 44, 4, 6};

    // Display all array elements using a for loop
    for(int i = 0; i <= 4; i++) {
        cout << arr[i] << " ";
    }

    return 0; // End of program
}
