#include<bits/stdc++.h>   // Includes all standard libraries
using namespace std;      // Use standard namespace

int main (){
    int x, y;             // Declare two integers
    x = 5;                // Assign 5 to x
    y = 7;                // Assign 7 to y

    cout << x + y << endl; // Print sum (12)
    cout << x - y << endl; // Print difference (-2)
    cout << x * y << endl; // Print product (35)
    cout << x / y << endl; // Print quotient (0, integer division)

    return 0;              // Exit program successfully
}
