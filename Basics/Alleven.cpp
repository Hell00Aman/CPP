#include <bits/stdc++.h>   // include all standard libraries
using namespace std;

int main() {

    // loop starts from 2 and runs till 100
    // i increases by 2 each time (so only even numbers are printed)
    for (int i = 2; i <= 100; i = i + 2) {
        cout << i << endl;  // print the current even number
    }

    return 0; // end of program
}
