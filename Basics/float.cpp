#include<bits/stdc++.h>   // Includes all standard C++ libraries
using namespace std;      // Use standard namespace

int main (){
    float x, y;           // Declare two float variables
    x = 5;                // Assign 5 to x
    y = 3;                // Assign 3 to y

    cout << x + y << endl; // Print sum (8)
    cout << x - y << endl; // Print difference (2)
    cout << x * y << endl; // Print product (15)
    cout << x / y << endl; // Print quotient (1.66667)

    return 0;              // Exit program successfully
}
