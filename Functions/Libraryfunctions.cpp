#include<bits/stdc++.h>
using namespace std;

int main() {
    // Square root of 9
    cout << sqrt(9) << endl;
    // Cube root of 8
    cout << cbrt(8) << endl;
    // Minimum of 3 and 8
    cout << min(3,8) << endl;
    // Maximum of 3 and 9
    cout << max(3,9) << endl;
    // 3 raised to power 3
    cout << pow(3,3) << endl;
}
